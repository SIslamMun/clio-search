#!/usr/bin/env python3
"""L2: IOWarp CTE BlobQuery scaling up to 1M blobs.

Runs the real iowarp-core main-branch build inside the iowarp/deps-cpu
Docker container. At each scale (1K, 5K, 10K, 50K, 100K, 500K, 1M):

  1. Start the Chimaera runtime inside the container
  2. Initialize the CTE client
  3. PutBlob N synthetic scientific blobs across 4 tags
  4. Measure BlobQuery latency for three query patterns:
       a) tag-filtered         (BlobQuery("temp_.*", ".*"))
       b) specific blob regex  (BlobQuery(".*", "blob_0000.*"))
       c) full scan            (BlobQuery(".*", ".*"))
  5. Record PutBlob throughput, total DB size, query latencies

Output
------
  eval/eval_final/outputs/L2_iowarp_cte_scaling.json
  eval/eval_final/plots/L2_iowarp_scaling_loglog.png   (generated by plotter)

Why this test
-------------
  * Real iowarp-core runtime — not a simulation
  * Main-branch wheel (already built at eval/eval_final/iowarp_core-1.0.3-*.whl)
    fixes the shared-memory bug in the PyPI v1.0.3 release
  * 1M blobs is the current ceiling on a single GH200-class node with RAM
    tier; beyond this we'd need the NVMe tier
  * Three query patterns reveal the scaling behaviour of CTE's different
    regex-match paths — tag-filter is sub-linear, full scan is linear.

Prerequisites
-------------
  * Docker running with `iowarp/deps-cpu:latest` pulled
  * Wheel at eval/eval_final/iowarp_core-1.0.3-cp312-cp312-linux_x86_64.whl
  * Ability to allocate --shm-size=8g (required for shared memory IPC
    between Chimaera runtime and client processes)

Usage
-----
  python3 eval/eval_final/code/laptop/L2_iowarp_cte_scaling.py

Estimated runtime
-----------------
  Scales 1K → 1M. At ~6000 blobs/sec write throughput observed in earlier
  runs, 1M blobs = ~170 sec write + ~30 sec query measurements per scale.
  Total: ~15-20 min for all scales.
"""

from __future__ import annotations

import json
import shutil
import subprocess
import sys
import tempfile
import textwrap
import time
from pathlib import Path

_REPO = Path(__file__).resolve().parents[4]
OUT_DIR = _REPO / "eval" / "eval_final" / "outputs"
WHEEL_PATH = _REPO / "eval" / "eval_final" / "iowarp_core-1.0.3-cp312-cp312-linux_x86_64.whl"
DOCKER_IMAGE = "iowarp/deps-cpu:latest"

# Scales to measure. Note the 1M entry — changed from earlier 100K.
SCALES = [1_000, 5_000, 10_000, 50_000, 100_000, 500_000, 1_000_000]


# ----------------------------------------------------------------------------
# Test script that runs INSIDE the Docker container.
# We pass this as a string to the container so the test is self-contained.
# ----------------------------------------------------------------------------

INNER_SCRIPT = textwrap.dedent("""
import subprocess
import time
import json
import random
import sys

from iowarp_core import wrp_cte_core_ext as cte

# --- Start runtime ---
rt_log = "/tmp/rt.log"
rt = subprocess.Popen(
    ["chimaera", "runtime", "start"],
    stdout=open(rt_log, "w"),
    stderr=subprocess.STDOUT,
)
for _ in range(20):
    time.sleep(1)
    try:
        if "Successfully started local server" in open(rt_log).read():
            break
    except Exception:
        pass

# --- Init client ---
cte.chimaera_init(cte.ChimaeraMode.kClient)
cte.initialize_cte("", cte.PoolQuery.Dynamic())
client = cte.get_cte_client()

# Scales are passed as an env var
import os
scales = json.loads(os.environ["SCALES_JSON"])

result = {"scales": []}
rng = random.Random(42)

for N in scales:
    print(f"--- Scale: {N} ---", flush=True)

    # Fresh tags per scale to avoid cross-contamination
    tags = {
        f"temp_{N}": cte.Tag(f"temp_{N}"),
        f"press_{N}": cte.Tag(f"press_{N}"),
        f"wind_{N}": cte.Tag(f"wind_{N}"),
        f"humid_{N}": cte.Tag(f"humid_{N}"),
    }
    tag_list = list(tags.values())

    # Phase 1: write N blobs spread across the four tags
    t0 = time.time()
    for i in range(N):
        tag = tag_list[i % 4]
        if i % 4 == 0:
            data = f"temperature {rng.uniform(0, 50):.2f} degC station {i}".encode()
        elif i % 4 == 1:
            data = f"pressure {rng.uniform(90, 110):.2f} kPa station {i}".encode()
        elif i % 4 == 2:
            data = f"wind {rng.uniform(0, 30):.2f} m/s station {i}".encode()
        else:
            data = f"humidity {rng.uniform(20, 90):.2f} percent station {i}".encode()
        tag.PutBlob(f"blob_{i:07d}", data)
    put_time = time.time() - t0
    put_throughput = N / put_time

    # Phase 2: three query patterns, each repeated 5 times, report median
    def timed_query(tag_regex, blob_regex, reps=5):
        times = []
        rows_found = 0
        for _ in range(reps):
            t = time.perf_counter()
            r = list(client.BlobQuery(tag_regex, blob_regex, N * 5, cte.PoolQuery.Dynamic()))
            times.append(time.perf_counter() - t)
            rows_found = len(r)
        times.sort()
        return rows_found, times[len(times) // 2] * 1000  # median in ms

    tag_rows, tag_ms = timed_query(f"temp_{N}", ".*")
    spec_rows, spec_ms = timed_query(".*", "blob_0000005")
    all_rows, all_ms = timed_query(".*", ".*")

    scale_result = {
        "N_blobs": N,
        "put_time_s": round(put_time, 3),
        "put_throughput_blobs_per_s": round(put_throughput, 0),
        "BlobQuery_tag_ms": round(tag_ms, 2),
        "BlobQuery_tag_results": tag_rows,
        "BlobQuery_specific_ms": round(spec_ms, 2),
        "BlobQuery_specific_results": spec_rows,
        "BlobQuery_all_ms": round(all_ms, 2),
        "BlobQuery_all_results": all_rows,
    }
    result["scales"].append(scale_result)
    print(json.dumps(scale_result, indent=2), flush=True)

# Teardown
rt.terminate()
try:
    rt.wait(timeout=5)
except Exception:
    rt.kill()

# Write structured result to stdout as a sentinel line
print("===RESULT_JSON_BEGIN===")
print(json.dumps(result))
print("===RESULT_JSON_END===")
""").strip()


def main() -> None:
    print("=" * 75)
    print("L2: IOWarp CTE BlobQuery scaling up to 1M blobs")
    print("=" * 75)

    if not WHEEL_PATH.exists():
        print(f"ERROR: wheel not found at {WHEEL_PATH}")
        print("Build it first with:")
        print("  docker run --rm -u root -v /tmp/clio-core:/src -v /tmp/iowarp-wheel:/out \\")
        print("    iowarp/deps-cpu:latest bash -c 'cp -r /src/. /build/ && cd /build && \\")
        print("    python3 -m pip wheel . --no-deps --wheel-dir=/out'")
        sys.exit(1)

    if shutil.which("docker") is None:
        print("ERROR: docker not found on PATH")
        sys.exit(1)

    with tempfile.TemporaryDirectory(prefix="L2_iowarp_") as tmp:
        tmp = Path(tmp)
        (tmp / "inner_test.py").write_text(INNER_SCRIPT)

        cmd = [
            "docker", "run", "--rm",
            "--shm-size=16g",
            "-u", "root",
            "-e", f"SCALES_JSON={json.dumps(SCALES)}",
            "-v", f"{WHEEL_PATH}:/wheels/{WHEEL_PATH.name}",
            "-v", f"{tmp / 'inner_test.py'}:/test.py",
            DOCKER_IMAGE,
            "bash", "-c",
            (
                f"python3 -m pip install /wheels/{WHEEL_PATH.name} "
                "--force-reinstall 2>&1 | tail -1 && "
                "python3 /test.py"
            ),
        ]

        print(f"\nRunning Docker container for scales {SCALES}...")
        print("(This will take ~15-20 min for 1M blob scale)")
        t0 = time.time()
        proc = subprocess.run(cmd, capture_output=True, text=True)
        elapsed = time.time() - t0
        print(f"\nDocker run wall time: {elapsed:.1f}s")

        stdout = proc.stdout
        partial_failure = proc.returncode != 0
        if partial_failure:
            print(f"\nDocker exited with code {proc.returncode} — parsing partial results")
            print("STDERR (last 10 lines):")
            for line in proc.stderr.splitlines()[-10:]:
                print(f"  {line}")

        # Extract the JSON sentinel block from stdout (full-run success)
        begin = stdout.find("===RESULT_JSON_BEGIN===")
        end = stdout.find("===RESULT_JSON_END===")
        if begin != -1 and end != -1:
            json_text = stdout[begin + len("===RESULT_JSON_BEGIN==="):end].strip()
            result = json.loads(json_text)
        else:
            # Fall back to parsing per-scale JSON blocks dumped before the crash
            result = {"scales": [], "partial": True}
            lines = stdout.splitlines()
            i = 0
            while i < len(lines):
                if "--- Scale:" in lines[i]:
                    # Find following { ... } JSON block
                    j = i + 1
                    while j < len(lines) and lines[j].lstrip()[:1] != "{":
                        j += 1
                    if j >= len(lines):
                        break
                    depth = 0
                    buf = []
                    while j < len(lines):
                        buf.append(lines[j])
                        depth += lines[j].count("{") - lines[j].count("}")
                        if depth == 0:
                            break
                        j += 1
                    try:
                        result["scales"].append(json.loads("\n".join(buf)))
                    except Exception:
                        pass
                    i = j + 1
                else:
                    i += 1
            if not result["scales"]:
                print("ERROR: no scale results recoverable from stdout")
                print("Full stdout tail:")
                print(stdout[-3000:])
                sys.exit(1)
            print(f"Recovered {len(result['scales'])} partial scale results")

    # Print summary table
    print("\n" + "=" * 75)
    print("SCALING RESULTS")
    print("=" * 75)
    print(
        f"{'Scale':>9} | {'Put(s)':>7} {'Throughput':>11} | "
        f"{'Tag ms':>7} {'Spec ms':>8} {'All ms':>8}"
    )
    print("-" * 75)
    for s in result["scales"]:
        print(
            f"{s['N_blobs']:>9} | "
            f"{s['put_time_s']:>7.2f} {s['put_throughput_blobs_per_s']:>9,.0f}/s | "
            f"{s['BlobQuery_tag_ms']:>7.2f} "
            f"{s['BlobQuery_specific_ms']:>8.2f} "
            f"{s['BlobQuery_all_ms']:>8.2f}"
        )

    # Scaling ratios
    if len(result["scales"]) >= 2:
        first = result["scales"][0]
        last = result["scales"][-1]
        scale_ratio = last["N_blobs"] / first["N_blobs"]
        tag_ratio = last["BlobQuery_tag_ms"] / first["BlobQuery_tag_ms"]
        spec_ratio = last["BlobQuery_specific_ms"] / first["BlobQuery_specific_ms"]
        all_ratio = last["BlobQuery_all_ms"] / first["BlobQuery_all_ms"]
        print("\nScaling ratios (last / first):")
        print(f"  Data growth:        {scale_ratio:.0f}x")
        print(f"  Tag-filtered query: {tag_ratio:.2f}x ({'sub-linear' if tag_ratio < scale_ratio else 'linear+'})")
        print(f"  Specific regex:     {spec_ratio:.2f}x")
        print(f"  Full scan:          {all_ratio:.2f}x")

    OUT_DIR.mkdir(parents=True, exist_ok=True)
    output = {
        "experiment": "L2: IOWarp CTE BlobQuery scaling up to 1M blobs",
        "timestamp": time.strftime("%Y-%m-%d %H:%M:%S"),
        "iowarp_version": "1.0.3 (main branch build)",
        "environment": f"Docker {DOCKER_IMAGE} with --shm-size=8g",
        "scales_requested": SCALES,
        "wall_time_s": round(elapsed, 1),
        **result,
    }
    out_path = OUT_DIR / "L2_iowarp_cte_scaling.json"
    with out_path.open("w") as f:
        json.dump(output, f, indent=2)
    print(f"\nSaved: {out_path}")


if __name__ == "__main__":
    main()
